def download_one(pid):
    pdf_path = PDF_DIR / f"{pid}.pdf"
    if pdf_path.exists() and pdf_path.stat().st_size > 10000:
        return True
    url = f"https://patentimages.storage.googleapis.com/pdfs/{pid}.pdf"
    try:
        r = requests.get(url, headers={"User-Agent":"Mozilla/5.0"}, timeout=60)
        if r.status_code == 200 and len(r.content) > 10000:
            pdf_path.write_bytes(r.content)
            return True
    except:
        pass
    return False

if __name__ == "__main__":
    n = len(ALL_PATENTS)
    print(f"Downloading {n} patents across {len(set(v.split('方法')[0][:6] for v in ALL_PATENTS.values()))} domains...")
    ok = fail = 0
    for i, (pid, title) in enumerate(ALL_PATENTS.items()):
        if download_one(pid):
            ok += 1; print(f"[{i+1:3d}/{n}] ✓ {pid}")
        else:
            fail += 1; print(f"[{i+1:3d}/{n}] ✗ {pid}")
        time.sleep(0.8)
    print(f"\n✓ {ok}/{n} downloaded to {PDF_DIR}")
    (Path.home()/"paper2patent"/"data"/"patents"/"manifest.json").write_text(
        json.dumps({"total":n,"downloaded":ok,"failed":fail,"patents":ALL_PATENTS},ensure_ascii=False,indent=2))
